// -----Logical AND && ---
/*
return true or false 
*/


console.log( true && true); // true
console.log(false && true); // false
console.log(5 > 4 && 'hello' != 'there'); // true
console.log('' && true); // ''



// Logical OR ||
console.log(".........................");

console.log(false || true); // true
console.log(false || false); // false
console.log('hello' || false); // 'hello'
console.log(false || ''); // ''